package entidades;

public class Patinete {

	 public String cor;
	 public double valor;
	 public String marca;
	
	 public Patinete(String cor, double valor, String marca) {
		super();
		this.cor = cor;
		this.valor = valor;
		this.marca = marca;
   }

	public Patinete(double valor, String marca) {
		super();
		this.valor = valor;
		this.marca = marca;
	}
	
	public Patinete(String marca) {
		super();
		this.marca = marca;
	}
	 
	 

	 
	 
	 
	 
}
